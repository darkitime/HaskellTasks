---Выбранный вариант (4):
---Дек (deque): структура данных с операциями записи элемента и чтения элемента в начало и в конец некоторой единой последовательности. 
---Если писать данные в начало, а считывать их с конца, дек будет работать как очередь; если и писать, и читать данные только с одного конца — как стек. 
---Рекомендуется обдумать использование для эффективной реализации не одного списка, а двух.

---Мотивировка выбора: В обработке сигналов (особенно когда речь идет о потоковых данных или длинных массивах) ключевую роль играет то, 
---как мы получаем доступ к элементам в определенный момент времени.
---FIR: Вычисление текущего отфильтрованного значения y[n] выглядит так:
---y[n] = b[0] * x[n] + b[1] * x[n-1] + ... + b[N-1] * x[n-N+1]
---где b[i] — коэффициенты фильтра, x[n-i] — входные данные, а N — порядок фильтра.
---При этом важно, что для вычисления y[n] нам нужны последние N входных данных (x[n], x[n-1], ..., x[n-N+1]). 
---Дек идеально подходит для хранения этих данных, так как мы можем легко добавлять новые данные в конец и удалять старые данные из начала, поддерживая таким образом "скользящее окно" последних N входных данных.
---Как это работает в динамике:
--- 1. Приходит новый отсчет x[n] — мы кладем его в начало дека.
--- 2. Если в деке уже есть N элементов, мы удаляем элемент из конца (x[n-N]), чтобы поддерживать размер окна.
--- 3. Теперь в деке всегда будут последние N входных данных, и мы можем легко вычислить y[n] с помощью коэффициентов b[i].
---Таким образом, дек обеспечивает эффективное управление данными для FIR-фильтра, позволяя нам быстро обновлять и получать доступ к последним N входным данным, что критично для производительности фильтра.
---Для БИХ-фильтров (IIR) логика та же самая, просто понадобится хранить историю не только входов x[n], но и предыдущих выходов y[n].

---Реализация:
--- 1. Базова структура данных (описание типа, функции удаления и добавления)
import Control.Monad.State ( modify, put, get, State, execState ) --- импортируем модуль для работы с монадами состояния
data Deque a = Deque [a] [a] --- типа данных Дека, хранящий два списка: один для начала, другой для конца
    deriving (Show) --- реализация класса Show для удобного отображения содержимого дека

emptyDeque :: Deque a --- функция для создания пустого дека
emptyDeque = Deque [] [] --- пустой дек представлен двумя пустыми списками

pushFront :: a -> Deque a -> Deque a --- функция для добавления элемента в начало дека
pushFront x (Deque front back) = Deque (x:front) back --- добавляем элемент x в начало списка front

popBack :: Deque a -> (Maybe a, Deque a) --- функция для удаления элемента из конца дека. Поскольку дек может быть пустым, возвращаем Maybe a для элемента и новый дек
popBack (Deque [] []) = (Nothing, emptyDeque) --- если оба списка пусты, дек пустой, возвращаем Nothing и пустой дек
popBack (Deque front (b:bs)) = (Just b, Deque front bs) --- если в списке back есть элементы, удаляем последний элемент и возвращаем его
popBack (Deque front []) = popBack (Deque [] (reverse front)) --- если в списке back нет элементов, но в front есть, переворачиваем front и используем его как back для удаления элемента

---2. Монада для дека:
--- Монада - это абстрактная структура данных, которая позволяет нам работать с контекстом (в данном случае, состоянием дека) и последовательно изменять его с помощью операций.
--- Это делается, чтобы упростить работу с состоянием и избежать необходимости явно передавать его между функциями.
--- Нам нужно сделать так, чтобы Дек стал невидимым фоном (контекстом), который обновляется сам по себе. 
---Для этого в Haskell используется встроенная монада State
--- Внутри монады State у нас есть три главные встроенные функции для работы с фоновым Деком:
---get — достает текущий Дек из фона, чтобы мы могли на него посмотреть.
---put — заменяет старый Дек в фоне на новый.
---modify — она берет функцию обновления (например, нашу pushFront) и применяет её к фоновому Деку напрямую.
---Превратим наши функции в монадические:

pushFrontM :: a -> State (Deque a) () --- функция для добавления элемента в начало дека внутри монады State
pushFrontM x = modify (pushFront x) --- используем modify, чтобы применить pushFront к текущему Деку в фоне

popBackM :: State (Deque a) (Maybe a) --- функция для удаления элемента из конца дека внутри монады State, возвращает Maybe a
popBackM = do
    currentDeque <- get --- получаем текущий Дек из фона
    let (result, newDeque) = popBack currentDeque --- применяем popBack к текущему Деку, получаем результат и новый Дек
    put newDeque --- обновляем фон новым Деком
    return result --- возвращаем результат удаления элемента

---3. Тесты для проверки работы функций:
---Поскольку наши функции pushFrontM и popBackM просто описывают действия с фоновым состоянием, нам нужен способ запустить эту цепочку в реальной программе. 
---Для этого используется встроенная функция execState. 
---Она принимает блок монадических действий и начальное состояние, применяет все шаги по очереди и возвращает финальный Дек
testDeque :: State (Deque Int) () --- тестовая функция для проверки работы pushFrontM, которая добавляет несколько элементов в дек
testDeque = do
    pushFrontM 10 
    pushFrontM 20
    pushFrontM 30

main :: IO ()
main = do
    let finalDeque = execState testDeque emptyDeque --- запускаем тестовую функцию с начальным состоянием пустого дека и получаем финальный Дек
    print finalDeque
    return ()
